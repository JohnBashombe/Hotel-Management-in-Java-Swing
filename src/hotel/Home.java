    private void roomsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_roomsTableMouseClicked

        try {
            int select = roomsTable.getSelectedRow();

            jLabel39.setText(roomsTable.getValueAt(select, 1).toString());
            jTextField2.setText(roomsTable.getValueAt(select, 2).toString());
            jTextField15.setText(roomsTable.getValueAt(select, 3).toString());
            String index = roomsTable.getValueAt(select, 4).toString();

            if ("Available".equals(index)) {
                jComboBox1.setEnabled(true);
                jComboBox1.setSelectedIndex(0);
                jComboBox1.removeItem("Occupied");
            }

            if ("Cleaning".equals(index)) {
                jComboBox1.setEnabled(true);
                jComboBox1.setSelectedIndex(1);
                jComboBox1.removeItem("Occupied");
            }
            if ("Has Problem".equals(index)) {
                jComboBox1.setEnabled(true);
                jComboBox1.setSelectedIndex(2);
                jComboBox1.removeItem("Occupied");
            }
            if ("Occupied".equals(index)) {
                jComboBox1.setEnabled(false);
                jComboBox1.addItem("Occupied");
                jComboBox1.setSelectedItem("Occupied");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "The error is : " + e.getMessage());
        }

    }//GEN-LAST:event_roomsTableMouseClicked

