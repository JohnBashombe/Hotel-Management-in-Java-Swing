            database.guestList();
            database.addToArchiveTable();
            database.totalAmount();

            //database.panelColor1();
            jTextField8.setText("");
            jTextField9.setText("");
            jTextField10.setText("");
            jTextField5.setText("");
            jTextField7.setText("");
            jLabel173.setText("Null");
